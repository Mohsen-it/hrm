class Database:
    """Simple DB abstraction supporting SQLite and MySQL."""

    def __init__(self, conn, db_type: str):
        self.conn = conn
        self.db_type = db_type

    def execute(self, sql: str, params: tuple = ()):
        """Execute SQL with proper placeholder handling."""
        if self.db_type == "mysql":
            sql = sql.replace("?", "%s")
            sql = sql.replace("INSERT OR IGNORE", "INSERT IGNORE")
            sql = sql.replace("INSERT OR REPLACE", "REPLACE")
        cur = self.conn.cursor()
        cur.execute(sql, params)
        # For auto-increment: if table has an explicit ID column and id is provided,
        # we shouldn't update auto-increment sequence
        return cur

    def executemany(self, sql: str, params_list: list[tuple]):
        """Execute SQL with multiple parameter sets."""
        if self.db_type == "mysql":
            sql = sql.replace("?", "%s")
            sql = sql.replace("INSERT OR IGNORE", "INSERT IGNORE")
            sql = sql.replace("INSERT OR REPLACE", "REPLACE")
        cur = self.conn.cursor()
        cur.executemany(sql, params_list)
        return cur

    def commit(self):
        """Commit pending changes."""
        self.conn.commit()

    def close(self):
        """Close the connection."""
        try:
            if self.db_type == "mysql":
                self.conn.close()
            else:
                self.conn.close()
        except Exception:
            pass

    def enable_foreign_keys(self):
        """Enable foreign key checks."""
        if self.db_type == "mysql":
            self.execute("SET FOREIGN_KEY_CHECKS=1")
        else:
            self.execute("PRAGMA foreign_keys=ON")


def open_db(db_path: Path, use_mysql: bool = False) -> Database:
    """Open database connection (SQLite or MySQL)."""
    if use_mysql:
        env_file = db_path  # db_path is actually the .env file path in mysql mode
        credentials = _read_env(env_file)
        conn = pymysql.connect(
            host=credentials["host"],
            port=credentials["port"],
            user=credentials["user"],
            password=credentials["password"],
            database=credentials["database"],
            charset="utf8mb4",
            autocommit=False,
        )
        db = Database(conn, "mysql")
        db.execute("SET FOREIGN_KEY_CHECKS=0")
        db.execute("SET NAMES utf8mb4")
        db.execute("SET CHARACTER SET utf8mb4")
        return db

    conn = sqlite3.connect(str(db_path))
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA synchronous=OFF")
    conn.execute("PRAGMA foreign_keys=OFF")
    return Database(conn, "sqlite")


def _read_env(env_path: Path) -> dict:
    """Read Laravel .env file and extract DB credentials."""
    creds = {
        "host": "127.0.0.1",
        "port": 3306,
        "user": "root",
        "password": "",
        "database": "laravel",
    }
    if not env_path.exists():
        print(f"WARNING: .env file not found at {env_path}, using defaults")
        return creds

    with open(env_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            if "=" in line:
                key, _, value = line.partition("=")
                key = key.strip()
                value = value.strip().strip('"').strip("'")
                if key == "DB_HOST":
                    creds["host"] = value
                elif key == "DB_PORT":
                    creds["port"] = int(value) if value else 3306
                elif key == "DB_USERNAME":
                    creds["user"] = value
                elif key == "DB_PASSWORD":
                    creds["password"] = value
                elif key == "DB_DATABASE":
                    creds["database"] = value
    return creds


def escape_str(val: str | None) -> str | None:
    """Escape string for SQL insertion."""
    if val is None:
        return None
    return val.replace("'", "''")


def safe_int(val: str | None, default: int | None = None) -> int | None:
    """Convert value to int safely."""
    if val is None or val.strip() == "":
        return default
    try:
        return int(val)
    except (ValueError, TypeError):
        return default


def safe_bool(val: str | None, default: bool = False) -> int:
    """Convert value to SQLite boolean (0/1)."""
    if val is None:
        return 1 if default else 0
    if isinstance(val, bool):
        return 1 if val else 0
    return 1 if val.lower() in ("t", "true", "1", "yes", "on") else 0


def safe_float(val: str | None, default: float | None = None) -> float | None:
    """Convert value to float safely."""
    if val is None or val.strip() == "":
        return default
    try:
        return float(val)
    except (ValueError, TypeError):
        return default


def now_ts() -> str:
    """Return current UTC timestamp string."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S")


def _normalize_ts(val: str | None) -> str | None:
    """Normalize a PostgreSQL timestamp to MySQL-compatible format."""
    if not val:
        return None
    # Remove timezone suffix: +08, +00:00
    val = re.sub(r"\+[0-9]{1,2}(:[0-9]{2})?$", "", val)
    # Truncate microseconds beyond 3 digits (MySQL max precision)
    val = re.sub(r"\.(\d{3})\d*", r".\1", val)
    # Replace T separator with space
    val = val.replace("T", " ")
    return val


def nullif(val: str | None) -> str:
    """Return 'NULL' if val is None, otherwise quote it."""
    if val is None:
        return "NULL"
    return f"'{escape_str(val)}'"


def nullif_int(val: int | None) -> str:
    """Return 'NULL' if val is None, otherwise the int."""
    if val is None:
        return "NULL"
    return str(val)


def nullif_float(val: float | None) -> str:
    """Return 'NULL' if val is None, otherwise the float."""
    if val is None:
        return "NULL"
    return str(val)


