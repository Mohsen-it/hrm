class Migrator:
    """Main migration class that orchestrates the data import."""

    def __init__(self, parser: PgDumpParser, db: Database | None, dry_run: bool = False):
        self.parser = parser
        self.db = db
        self.is_mysql = db is not None and db.db_type == "mysql"
        self.dry_run = dry_run
        self.id_maps: dict[str, dict[int, int]] = {}  # old_id -> new_id maps
        self.stats: dict[str, int] = {}

    def exec(self, sql: str, params: tuple = ()) -> None:
        """Execute SQL if not dry run."""
        if self.dry_run:
            return
        try:
            self.db.execute(sql, params)
        except Exception as e:
            print(f"  SQL ERROR: {e}")
            print(f"  SQL: {sql[:200]}...")
            raise

    def commit(self) -> None:
        """Commit pending changes."""
        if not self.dry_run:
            self.db.commit()

    def insert_batch(self, table: str, columns: list[str], rows: list[list[str | None]]) -> int:
        """Insert multiple rows into the database."""
        if not rows:
            return 0

        cols = ", ".join(columns)
        # generate placeholders compatible with the DB driver
        placeholders = ", ".join(["?" for _ in columns])
        sql = f"INSERT INTO {table} ({cols}) VALUES ({placeholders})"

        count = 0
        for row in rows:
            if self.dry_run:
                count += 1
            else:
                try:
                    db_row = tuple(row)
                    self.db.execute(sql, db_row)
                    count += 1
                except Exception as e:
                    # Only print first few errors
                    pass
        return count

