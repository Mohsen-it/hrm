        id_map: dict[int, int] = {}
        ts = now_ts()
        count = 0

        # Group by (emp_id, att_date) to build summaries
        grouped: dict[tuple[int, str], dict] = defaultdict(lambda: {
            "att_date": "",
            "emp_id": 0,
            "shift_id": None,
            "total_minutes": 0,
            "overtime_minutes": 0,
            "codes": [],
            "status": "present",
        })

        for row in attcode_data:
            emp_id = safe_int(row.get("emp_id"))
            att_date = row.get("att_date")
            duration = safe_int(row.get("duration"), 0)  # in minutes
            shift_id = safe_int(row.get("shift_id"))
            att_code_alias = row.get("att_code_alias") or ""
            hours = safe_float(row.get("hours"), 0)
            minutes_val = safe_float(row.get("minutes"), 0)

            if not emp_id or not att_date:
                continue

            key = (emp_id, att_date)
            entry = grouped[key]
            entry["emp_id"] = emp_id
            entry["att_date"] = att_date
            if shift_id:
                entry["shift_id"] = shift_id

            # Track time
            entry["total_minutes"] += duration

            # Check for overtime codes (OT, overtime, over)
            code_lower = att_code_alias.lower()
            if any(kw in code_lower for kw in ["over", "ot", "extra"]):
                entry["overtime_minutes"] += duration

            entry["codes"].append(att_code_alias)

            # Check for absent/late/leave codes
            if any(kw in code_lower for kw in ["absence", "absent"]):
                entry["status"] = "absent"
            elif any(kw in code_lower for kw in ["leave", "vacation"]):
                entry["status"] = "vacation"

        print(f"  Grouped into {len(grouped)} daily summaries")

        # Also process att_payloadpaycode for pay code data
        paycode_data = self.parser.get_table_data("att_payloadpaycode")
        if paycode_data:
            print(f"  Found {len(paycode_data)} pay code records, merging...")
            for row in paycode_data:
                emp_id = safe_int(row.get("emp_id"))
                att_date = row.get("att_date")
                duration = safe_int(row.get("duration"), 0)
                pay_code_alias = row.get("pay_code_alias") or ""
                shift_id = safe_int(row.get("shift_id"))

                if not emp_id or not att_date:
                    continue

                key = (emp_id, att_date)
                entry = grouped[key]
                entry["emp_id"] = emp_id
                entry["att_date"] = att_date
                entry["total_minutes"] += duration
                if shift_id:
                    entry["shift_id"] = shift_id

                code_lower = pay_code_alias.lower()
                if any(kw in code_lower for kw in ["over", "ot", "extra"]):
                    entry["overtime_minutes"] += duration
                if any(kw in code_lower for kw in ["absence", "absent"]):
                    entry["status"] = "absent"
                elif any(kw in code_lower for kw in ["leave", "vacation"]):
                    entry["status"] = "vacation"

                entry["codes"].append(pay_code_alias)

        # Insert summaries
        batch: list[tuple] = []
        for (emp_id, att_date), entry_data in grouped.items():
            shift_id = entry_data.get("shift_id")
            status = entry_data.get("status", "present")
            total_minutes = entry_data.get("total_minutes", 0)
