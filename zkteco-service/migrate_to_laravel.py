        self.commit()
        print(f"  Migrated {log_count} raw logs (skipped {skip_count})")
        print()
        print("  NOTE: Attendance sessions are NOT created during migration.")
        print("  Run the following command to process raw logs into sessions:")
        print("    php artisan attendance:process-raw-logs")
        print()
        return id_map

