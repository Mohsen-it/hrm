class PgDumpParser:
    """Parse a PostgreSQL dump file and extract COPY data."""

    def __init__(self, filepath: Path):
        self.filepath = filepath
        self._tables_data: dict[str, list[list[str]]] = {}
        self._table_columns: dict[str, list[str]] = {}

    def parse(self, tables: list[str] | None = None) -> None:
        """Parse the dump file and extract data for the specified tables.

        Args:
            tables: List of table names to extract. If None, extract all.
        """
        print(f"Parsing {self.filepath} ({self.filepath.stat().st_size / 1024 / 1024:.1f} MB)...")

        current_table = None
        current_cols: list[str] = []
        buf: list[str] = []
        in_copy = False
        line_count = 0

        with open(self.filepath, "r", encoding="utf-8", errors="replace") as f:
            for raw_line in f:
                line = raw_line.rstrip("\n").rstrip("\r")

                if in_copy:
                    if line == "\\.":
                        # End of COPY data
                        if current_table and (tables is None or current_table in tables):
                            self._table_columns[current_table] = current_cols
                            self._tables_data[current_table] = self._parse_data_rows(buf, len(current_cols))
                            print(f"  ✓ {current_table}: {len(self._tables_data[current_table])} rows")
                        in_copy = False
                        current_table = None
                        current_cols = []
                        buf = []
                    else:
                        buf.append(line)
                    continue

                m = re.match(r"COPY public\.(\w+)\s*\((.*?)\)\s+FROM stdin;", line)
                if m:
                    table_name = m.group(1)
                    cols_str = m.group(2)
                    current_cols = [c.strip() for c in cols_str.split(",")]
                    in_copy = True
                    current_table = table_name
                    buf = []
                    continue

                line_count += 1
                if line_count % 100000 == 0:
                    pass  # silent progress

        print(f"Parsing complete. {len(self._tables_data)} tables extracted.")
        print()

    def _parse_data_rows(self, lines: list[str], col_count: int) -> list[list[str]]:
        """Parse tab-separated data lines into rows of column values."""
        rows: list[list[str]] = []
        for line in lines:
            if not line.strip():
                continue
            # Split by tab, handling \N as NULL
            values = line.split("\t")
            # Pad or truncate to match column count
            if len(values) < col_count:
                values.extend(["\\N"] * (col_count - len(values)))
            elif len(values) > col_count:
                values = values[:col_count]
            rows.append(values)
        return rows

    def get_table_data(self, table: str) -> list[dict[str, str | None]]:
        """Get table data as list of dicts with column name keys."""
        if table not in self._tables_data:
            return []

        cols = self._table_columns.get(table, [])
        rows = self._tables_data[table]
        result: list[dict[str, str | None]] = []

        for row in rows:
            data: dict[str, str | None] = {}
            for i, col in enumerate(cols):
                if i < len(row):
                    val = row[i]
                    data[col] = None if val == "\\N" else val
                else:
                    data[col] = None
            result.append(data)

        return result

    @property
    def available_tables(self) -> list[str]:
        return sorted(self._tables_data.keys())


