    # Step 1: Companies
    # ==================================================================

    def migrate_companies(self) -> dict[int, int]:
        """Import companies from personnel_company to companies table."""
        print("=" * 60)
        print("Step 1: Migrating Companies")
        print("=" * 60)

        data = self.parser.get_table_data("personnel_company")
        print(f"  Found {len(data)} companies in dump")

        id_map: dict[int, int] = {}
        ts = now_ts()
        count = 0

        for row in data:
            old_id = safe_int(row.get("id"))
            company_code = row.get("company_code") or f"CMP{old_id:04d}"
            company_name = row.get("company_name") or f"Company {old_id}"
            is_default = safe_bool(row.get("is_default"))
            address = row.get("address")
            address2 = row.get("address2")
            email = row.get("email")
            phone = row.get("phone")
            city = row.get("city")
            country = row.get("country")
