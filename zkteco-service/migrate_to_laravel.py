def main():
    parser = argparse.ArgumentParser(
        description="Migrate ZKTeco PostgreSQL dump to Laravel HRM database"
    )
    parser.add_argument(
        "--db",
        type=Path,
        default=DEFAULT_DB,
        help=f"Path to SQLite database (default: {DEFAULT_DB})",
    )
    parser.add_argument(
        "--dump",
        type=Path,
        default=DEFAULT_DUMP,
        help=f"Path to PostgreSQL dump file (default: {DEFAULT_DUMP})",
    )
    parser.add_argument(
        "--step",
        type=str,
        default="all",
        choices=[
            "all", "companies", "branches", "departments", "positions",
            "zones", "shifts", "employees", "devices", "attendance",
            "holidays", "vacations", "fingerprints", "daily_summaries",
            "employee_groups",
        ],
        help="Migration step to run (default: all)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Parse dump without inserting data",
    )
    parser.add_argument(
        "--start-from",
        type=str,
        default=None,
        choices=[
            "companies", "branches", "departments", "positions",
            "zones", "shifts", "employees", "devices", "attendance",
            "holidays", "vacations", "fingerprints", "daily_summaries",
            "employee_groups",
        ],
        help="Start migration from this step (inclusive)",
    )
    parser.add_argument(
        "--mysql",
        action="store_true",
        help="Connect to MySQL database using .env credentials",
    )
    args = parser.parse_args()

    # Validate files
    if not args.dump.exists():
        print(f"ERROR: Dump file not found: {args.dump}")
        sys.exit(1)

    use_mysql = args.mysql

    if not args.dry_run:
        if use_mysql:
            if not HAS_PYMYSQL:
                print("ERROR: pymysql is not installed. Run:")
                print("  pip install pymysql")
                sys.exit(1)
            env_file = DEFAULT_ENV
            if not env_file.exists():
                print(f"ERROR: .env file not found at {env_file}")
                sys.exit(1)
        elif not args.db.exists():
            print(f"ERROR: Database file not found: {args.db}")
            print("Make sure you've run the Laravel migrations first:")
            print("  php artisan migrate")
            sys.exit(1)

    print("=" * 60)
    print("HRM Data Migration Tool")
    print("=" * 60)
    print(f"Dump file:   {args.dump}")
    print(f"Database:    {'MySQL (.env)' if use_mysql else args.db}")
    print(f"Dry run:     {args.dry_run}")
    print(f"Step:        {args.step}")
    if args.start_from:
        print(f"Start from:  {args.start_from}")
    print()

    start_time = time.time()

    # Parse dump
    tables_to_parse: list[str] | None = None
    if args.step != "all":
        # Map step to required tables
        step_tables: dict[str, list[str]] = {
            "companies": ["personnel_company"],
            "branches": [],  # No source table
            "departments": ["personnel_department"],
            "positions": ["personnel_position"],
            "zones": ["personnel_area"],
            "shifts": ["att_timeinterval"],
            "employees": ["personnel_employee"],
            "devices": ["iclock_terminal"],
            "attendance": ["iclock_transaction"],
            "holidays": ["att_holiday"],
            "vacations": ["att_leave", "att_paycode"],
            "fingerprints": ["iclock_biodata"],
            "daily_summaries": ["att_payloadattcode", "att_payloadpaycode"],
            "employee_groups": ["att_attgroup", "att_attemployee"],
        }
        tables_to_parse = step_tables.get(args.step, [])
        if not tables_to_parse:
            print(f"No tables to parse for step '{args.step}'. Parsing all.")
            tables_to_parse = None

    pg_parser = PgDumpParser(args.dump)
    pg_parser.parse(tables=tables_to_parse)

    # Open database
    db = None if args.dry_run else open_db(args.db if not use_mysql else DEFAULT_ENV, use_mysql=use_mysql)

    try:
        migrator = Migrator(pg_parser, db, dry_run=args.dry_run)

        # Define step order
        all_steps = [
            "companies", "branches", "departments", "positions",
            "zones", "shifts", "employees", "devices", "attendance",
            "holidays", "vacations", "fingerprints", "daily_summaries",
            "employee_groups",
        ]

        # Filter steps
        if args.step == "all":
            if args.start_from:
                try:
                    start_idx = all_steps.index(args.start_from)
                    steps = all_steps[start_idx:]
                except ValueError:
                    print(f"Unknown step: {args.start_from}")
                    sys.exit(1)
            else:
                steps = all_steps
        else:
            steps = [args.step]

        # Run migration steps
        for step in steps:
            step_method = getattr(migrator, f"migrate_{step}", None)
            if step_method is None:
                print(f"WARNING: No migration method for step '{step}'")
                continue

            result = step_method()
            migrator.stats[step] = len(result) if result else 0

    finally:
        if db:
            db.commit()
            db.enable_foreign_keys()
            db.close()

    elapsed = time.time() - start_time
    print("=" * 60)
    print("Migration Summary")
    print("=" * 60)
    for step, count in migrator.stats.items():
        print(f"  {step:15s}: {count:>8d} records")
    print(f"\n  Total time: {elapsed:.1f} seconds")
    print()

    if args.dry_run:
        print("DRY RUN COMPLETE - No data was inserted.")
    else:
        print("Migration complete!")
        print()
        print("Next steps:")
        print("  1. Run attendance processing command:")
        print("     php artisan attendance:process-raw-logs")
        print("  2. Generate daily summaries:")
        print("     php artisan attendance:generate-daily-summaries")
        print("  3. Verify data in the admin panel")


if __name__ == "__main__":
    main()
